_KEYWORDS: tuple[str, ...] = _COUNTRIES + _CITIES

# 单词边界：中文不需要 \b；用「负向后顾 + 全词匹配」也复杂，这里直接子串匹配。
_PATTERN = re.compile("|".join(map(re.escape, _KEYWORDS)))


def detect_overseas(text: str) -> str | None:
    """返回输入中第一个命中的境外目的地关键词，无命中返回 None。"""
    if not text:
        return None
    m = _PATTERN.search(text)
    return m.group(0) if m else None
